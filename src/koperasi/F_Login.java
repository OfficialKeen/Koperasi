    private void buttonTransparan5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTransparan5ActionPerformed
        try {
            Statement statement=(Statement)Koneksi.GetConnection().createStatement();
            ResultSet res=statement.executeQuery("select * from t_user where user='"+txname.getText()+"'");
            if (res.next()){
                if(txpass.getText().equals(res.getString("Pass"))){
                    JOptionPane.showMessageDialog(null, "Log-In Berhasil");
                    new F_Utama().show();
                    F_Login.this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null,"Password Salah !!!");
                    txpass.setText("");
                }
            }
            else{
                JOptionPane.showMessageDialog(null,"Username dan Password Tidak Tersedia");
                txname.setText("");
                txpass.setText("");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"terjadi kesalahan" + e);
        }
    }//GEN-LAST:event_buttonTransparan5ActionPerformed

